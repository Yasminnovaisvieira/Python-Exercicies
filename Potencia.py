# Escreva um programa que calcule a potência de um número inteiro elevado a um expoente.

print(f"Olá! O programa irá calcular a POTÊNCIA de um número inteiro elevado a um expoente.\n")

base = int(input("Insira o número Base: "))
expoente = int(input("Insira o número Expoente: "))

resultado = base ** expoente

print(f"{base} elevado a {expoente} é igual a: {resultado}")